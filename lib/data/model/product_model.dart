class Product {
  final String id;
  final String category;
  final String size;
  final double price;
  final String title;
  final String subtitle;
  final String description;
  final String images;
  final double rating;

  Product({
    required this.id,
    required this.category,
    required this.size,
    required this.price,
    required this.title,
    required this.subtitle,
    required this.description,
    required this.images,
    required this.rating,
  });
}
